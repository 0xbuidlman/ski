import UIKit

extension UIColor {
    static func c64blueColor() -> UIColor {
        return UIColor(red: 72/255.0, green: 58/255.0, blue: 170/255.0, alpha: 1)
    }
    static func c64brownColor() -> UIColor {
        return UIColor(red: 147/255.0, green: 73/255.0, blue: 64/255.0, alpha: 1)
    }
}